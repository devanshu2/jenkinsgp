package com.groupon.go.model;

public class NavDrawerItemModel {

	private String	title;
	private int		img_id;

	/**
	 * @return the title
	 */
	public String getTitle() {
		return title;
	}

	/**
	 * @param title
	 *            the title to set
	 */
	public void setTitle(String tittle) {
		this.title = tittle;
	}

	/**
	 * @return the img_id
	 */
	public int getImg_id() {
		return img_id;
	}

	/**
	 * @param img_id
	 *            the img_id to set
	 */
	public void setImg_id(int img_id) {
		this.img_id = img_id;
	}
}
